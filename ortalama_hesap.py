# Kullanıcıdan 2 adet not alınacak
mat = float(input("Matematik notunuzu giriniz: "))  # matematik notu
ing = float(input("İngilizce notunuzu giriniz: "))  # ingilizce notu

ortalama = (mat + ing) / 2    # ortalama hesabı

# Kullanıcıya not ortalaması gösterilecek
print(f"Not ortalamanız {ortalama}'dır.")
